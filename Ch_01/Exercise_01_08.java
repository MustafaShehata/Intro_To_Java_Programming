
package Ch_01;

/*
(Area and perimeter of a circle) Write a program that displays the area and perimeter
of a circle that has a radius of 5.5 using the following formulas:
        perimeter = 2 * radius * p
        area = radius * radius * p
*/

public class Exercise_01_08 {
    public static void main(String[] args) {
        
        System.out.println("The perimeter of a circle = " + 2 * 5.5 * 3.14 );
        System.out.println("The area of a circle = " + 5.5 * 5.5 * 3.14);
        
    }
}
